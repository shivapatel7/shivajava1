package shivajava;

public class LoopWhile {
    public static void main(String[] args) {
        int i =10;
        System.out.println("to print 10 to 1 number with while loop");
        while (i>=1) {
            System.out.println(i);
            i--;
        }
        System.out.println(" to print 10 to 1 number with Do while loop");
        int x =10;
        do {
            System.out.println(x);
            x--;
        }while (x>=1);
    }
}
